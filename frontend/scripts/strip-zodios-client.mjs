#!/usr/bin/env node
/**
 * Strip the @zodios/core runtime client from openapi-zod-client output.
 *
 * We use the generator only for its zod schemas — the Zodios endpoints
 * client at the bottom of the file (and its `@zodios/core` import) would
 * pull a runtime dep we don't want. This post-processor removes both,
 * leaving a clean schemas-only module.
 *
 * Usage: node scripts/strip-zodios-client.mjs <path-to-generated-file>
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { argv, exit } from 'node:process';

const file = argv[2];
if (!file) {
  console.error('usage: strip-zodios-client.mjs <file>');
  exit(2);
}

const src = readFileSync(file, 'utf8');

// Drop the zodios import — we only ever needed the schemas.
const noImport = src.replace(
  /^import\s*\{\s*makeApi,\s*Zodios,\s*type\s+ZodiosOptions\s*\}\s*from\s*"@zodios\/core";\s*\n/m,
  '',
);

// Truncate at the endpoints array — everything below is Zodios runtime.
const endpointsAt = noImport.indexOf('const endpoints = makeApi(');
if (endpointsAt === -1) {
  // Already stripped or template changed — leave file alone.
  writeFileSync(file, noImport);
  exit(0);
}

const stripped = noImport.slice(0, endpointsAt).replace(/\s*$/, '\n');
const banner =
  '// AUTO-GENERATED by `npm run gen:zod`. Edit upstream OpenAPI instead.\n' +
  '// Zodios client stripped via scripts/strip-zodios-client.mjs.\n\n';

writeFileSync(file, banner + stripped);
